/* bin_print.c ― 정수를 2진수로 바꿔 한 줄에 출력 */

#include <stdio.h>

/* 0 ≤ n ≤ 2 100 000 000 범위만 허용   ─────────────────────────── */
void bin(int n)
{
    if (n < 0 || n > 2100000000) {
        puts("Not available");   /* puts는 문자열 뒤에 개행을 붙여 준다 */
        return;
    }

    /* n이 0이면 바로 "0" 출력 후 종료 */
    if (n == 0) {
        puts("0");
        return;
    }

    /* 32 bit 정수라면 최대 31 개의 비트 + 널 문자 1 개 */
    /* (2100000000은 0b11111001100111100110000000000, 31 bit) */
    char buf[32 + 1];
    int  idx = 32;               /* 마지막 요소는 널 종료 문자 위치 */
    buf[idx] = '\0';

    /* -------- 나눗셈·나머지(div / mod) 방식 -------- */
    while (n > 0) {
        buf[--idx] = '0' + (n % 2);  /* 짝수→'0', 홀수→'1' */
        n /= 2;                      /* 다음 비트를 위해 2로 나눔 */
    }

    puts(&buf[idx]);             /* 완성된 문자열 한꺼번에 출력 */
}

int main(void)
{
    bin(10);         /* 1010 */
    bin(0);          /* 0 */
    bin(1);          /* 1 */
    bin(2);          /* 10 */
    bin(1024);       /* 10000000000 */
    bin(2100000001); /* Not available */

    return 0;
}
